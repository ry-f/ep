#include <unistd.h>

void	ft_print_numbers(void)
{
	char	c;

	c = '0';
	write (1, &c, 1);
	c = '1';
	write (1, &c, 1);
	c = '2';
	write (1, &c, 1);
	c = '3';
	write (1, &c, 1);
	c = '4';
	write (1, &c, 1);
	c = '5';
	write (1, &c, 1);
	c = '6';
	write (1, &c, 1);
	c = '7';
	write (1, &c, 1);
	c = '8';
	write (1, &c, 1);
	c = '9';
	write (1, &c, 1);
	c = '\n';
	write (1, &c, 1);
}
