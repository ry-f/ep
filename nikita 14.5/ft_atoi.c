int	ft_atoi(const char *str)
{
	int result;
	int sign;

	result = 0;
	sign = 1;
	if (*str == '-')
	{
		str++;
		sign = -1;
	}
	while (*str >= '0' && *str <= '9')
	{
		result *= 10;
		result += *str++ - '0';
	}
	return sign * result;
}
