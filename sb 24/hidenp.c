#include <unistd.h>

int			hidenp(char *s1, char *s2)
{
	while (*s1)
	{
		if (*s1 == *s2)
			s2++;
		s1++;
	}
	return (*s2 == '\0');
}

int			main(int argc, char **argv)
{
	if (argc != 3)
	{
		write(1, "\n", 1);
		return (0);
	}
	if (hidenp(argv[2], argv[1]))
		write(1, "1\n", 2);
	else
		write(1, "0\n", 2);
	return (0);
}
