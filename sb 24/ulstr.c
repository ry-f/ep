#include <unistd.h>

void		betputchar(char c)
{
	if (c >= 'a' && c <= 'z')
		c += 'A' - 'a';
	else if (c >= 'A' && c <= 'Z')
		c += 'a' - 'A';
	write(1, &c, 1);
}

int			main(int ac, char **av)
{
	int		i;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
			betputchar(av[1][i++]);
	}
	betputchar('\n');
	return (0);
}
