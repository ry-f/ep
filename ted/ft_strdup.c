#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		length;
	char	*dest;
	int		i;

	length = 0;
	while (src[length++]);
	dest = (char*)malloc(sizeof(*src) * length);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
