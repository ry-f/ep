#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		length;
	char	*new;
	int		i;

	if (!src)
		return (NULL);
	length = 0;
	while (src[length])
		length++;
	if (!(new = (char *)malloc(length + 1)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
